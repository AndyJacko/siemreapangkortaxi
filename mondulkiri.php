                <h1>Mondulkiri</h1>
                <p>Mondulkiri is a province of Cambodia. Bordering the provinces of Kratié to the west, Stung Treng to the northwest, Ratanakiri to the north and the country of Vietnam to the east and south, it is the most sparsely populated province in the country despite being the largest in land area. The province was created in 1961 from the eastern part of Kratié Province. The capital is the town of Senmonorom.</p>
                <p>In 1960 Mondulkiri was created out of Kratié Province by order of King Norodom Sihanouk. Sen Monorom was set up as the capital in 1962. During the Vietnam War in the late 1960s, Mondulkiri was home to three bases of the National Liberation Front of South Vietnam which resulted in numerous incursions and bombings by US forces. About 1970 the area fell under Khmer Rouge domination. Subsequently much of the populace was forcibly removed to Koh Nhek (Kaoh Nheaek) district to provide labor for rice growing. Schools, hospitals, even entire villages were destroyed. As many as half of the people in the province died during the forced relocation in the 1970s.</p>
                <p>The Bunong people appear to have lived in the provincial area for about 2000 years, but there is little documentation until France colonized Cambodia in 1864. Like other people in the province, the Bunong were displaced in the 1970s and only allowed to return to their traditional homeland in the 1980s.</p>
