                <h1>Sihanoukville</h1>
                <p>Sihanoukville Province is a province in the south-west of Cambodia at the Gulf of Thailand. The provincial capital, also called Sihanoukville, is a deep-water port city, the country's premier tourist destination and a steadily growing and diversifying urban center located on an elevated peninsula.</p>
                <p>The province is named in honor of former king Norodom Sihanouk, who personally orchestrated the establishment of Sihanoukville city and the Sihanoukville municipality as this took place alongside the construction of the Sihanoukville Port, which commenced in June 1955. The only deep water port of Cambodia includes a mineral Oil terminal and a transport logistics facility.</p>
                <p>Sihanoukville Province is divided into four districts, each with a distinct economic character, defined largely by location and access to resources. In addition to the port and the growing tourism industry, the activities of countless NGO's and international investment have contributed to an unprecedented economic growth of the province over the course of the last decade. Economic sectors that deserve mentioning are the transport & logistics industry, process manufacturing, agriculture &amp; fishery, textile industry and the real estate market.</p>
                <p>The islands and beaches of Sihanoukville province are an international tourist destination as visitor numbers have risen steadily since the late 20th century.</p>
                <p>The initial Sihanoukville municipality was elevated to a regular province on 22 December 2008 after King Norodom Sihamoni signed a Royal Decree converting the municipalities of Kep, Pailin and Sihanoukville into provinces, as well as incorporating Kompong Seila district. As one of Cambodia's agriculturally and industrially most diverse province its economic future has a solid basis, although the essential sectors agriculture and tourism require a strict and permanent administrative protection of the local natural resources.</p>
