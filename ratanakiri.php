                <h1>Ratanakiri</h1>
                <p>Ratanakiri is a province of Cambodia located in the remote northeast. It borders the provinces of Mondulkiri to the south and Stung Treng to the west and the countries of Laos and Vietnam to the north and east, respectively. The province extends from the mountains of the Annamite Range in the north, across a hilly plateau between the Tonle San and Tonle Srepok rivers, to tropical deciduous forests in the south. In recent years, logging and mining have scarred Ratanakiri's environment, long known for its beauty.</p>
                <p>For over a millennium, Ratanakiri has been occupied by the highland Khmer Loeu people, who are a minority elsewhere in Cambodia. During the region's early history, its Khmer Loeu inhabitants were exploited as slaves by neighboring empires. The slave trade economy ended during the French colonial era, but a harsh Khmerization campaign after Cambodia's independence again threatened Khmer Loeu ways of life. The Khmer Rouge built its headquarters in the province in the 1960s, and bombing during the Vietnam War devastated the region. Today, rapid development in the province is altering traditional ways of life.</p>
                <p>Ratanakiri is sparsely populated; its 150,000 residents make up just over 1% of the country's total population. Residents generally live in villages of 20 to 60 families and engage in subsistence shifting agriculture. Ratanakiri is among the least developed provinces of Cambodia. Its infrastructure is poor, and the local government is weak. Health indicators in Ratanakiri are extremely poor; men's life expectancy is 39 years, and women's is 43 years. Education levels are also low, with just under half of the population illiterate.</p>
